    private void GUIConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GUIConsultarActionPerformed
        String nb = GUIbuscaName.getText();
        String ape1 = GUIbuscaApellido.getText();
        String ape2 = GUIbuscaApellido2.getText();
        String msjFinal = "";
        try {
            ResultSet resultado = sentenciador.executeQuery("SELECT * FROM Datosagenda WHERE nombre='"+nb+"' AND primer_apellido='"+ape1+"' AND segundo_apellido='"+ape2+"'");
            while(resultado.next()){
                int cedResultado = resultado.getInt("CEDULA");
                String nbResultado = resultado.getString("NOMBRE");
                String ape1Resultado = resultado.getString("PRIMER_APELLIDO");
                String ape2Resultado = resultado.getString("SEGUNDO_APELLIDO");
                int telResultado = resultado.getInt("TELEFONO");
                String corrResultado = resultado.getString("CORREO_ELECTR√ìNICO");// CREO Q AH√ç ESTA EL PROB. CORREO_ELECTRONICO
                msjFinal += "Cedula: " + cedResultado + "\n" + "Nombre: " + nbResultado + "\n" + "Primer Apellido: " + ape1Resultado + "\n" 
                        + "Segundo Apellido: " + ape2Resultado + "\n" + "Telefono: " + telResultado + "\n" + "Correo: " + corrResultado + "\n" 
                        + "--------------------------------------\n";
            }
            GUItxtbuscar.setText(msjFinal);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "ERROR DE SELECT!\n" + e);
        }

    }//GEN-LAST:event_GUIConsultarActionPerformed

